INSERT INTO public.roles_permissions (role_id, permission_id)
SELECT r.id, p.id
FROM public.roles r
CROSS JOIN public.permissions p
WHERE r.slug = 'admin' AND r.agency_id IS NULL
  AND p.slug IN (
      'view-webhook-alert-config',
      'create-webhook-alert-config',
      'update-webhook-alert-config',
      'delete-webhook-alert-config',
      'view-webhook-alert-config-m',
      'view-webhook-alert-config-ma',
      'create-webhook-alert-config-m',
      'create-webhook-alert-config-ma',
      'update-webhook-alert-config-m',
      'update-webhook-alert-config-ma',
      'delete-webhook-alert-config-m',
      'delete-webhook-alert-config-ma'
  )
  AND NOT EXISTS (
      SELECT 1 FROM public.roles_permissions rp
      WHERE rp.role_id = r.id AND rp.permission_id = p.id
  );
