INSERT INTO public.roles_permissions (role_id, permission_id)
SELECT r.id, p.id
FROM public.roles r
CROSS JOIN public.permissions p
WHERE r.slug = 'admin' AND r.agency_id IS NULL
  AND p.slug IN (
      'view-branch',
      'create-branch',
      'update-branch',
      'delete-branch',
      'view-branch-m',
      'view-branch-ma',
      'create-branch-m',
      'create-branch-ma',
      'update-branch-m',
      'update-branch-ma',
      'delete-branch-m',
      'delete-branch-ma'
  )
  AND NOT EXISTS (
      SELECT 1 FROM public.roles_permissions rp
      WHERE rp.role_id = r.id AND rp.permission_id = p.id
  );
