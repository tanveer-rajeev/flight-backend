--
-- Name: user_balance_reconciliation; Type: VIEW; Schema: public; Owner: -
--

CREATE VIEW public.user_balance_reconciliation AS
 SELECT u.id AS user_id,
    b.company_name AS agency_name,
    round((COALESCE(t.total_credit, (0)::double precision))::numeric, 2) AS total_credit,
    round((COALESCE(t.total_debit, (0)::double precision))::numeric, 2) AS total_debit,
    round(((COALESCE(t.total_credit, (0)::double precision) - COALESCE(t.total_debit, (0)::double precision)))::numeric, 2) AS deb_cred_balance,
    round((u.balance)::numeric, 2) AS user_balance,
    round((((COALESCE(t.total_credit, (0)::double precision) - COALESCE(t.total_debit, (0)::double precision)) - u.balance))::numeric, 2) AS diff_amount
   FROM ((public.users u
     LEFT JOIN public.businesses b ON ((b.mother_user_id = u.id)))
     LEFT JOIN ( SELECT transactions.user_id,
            sum(
                CASE
                    WHEN (upper((transactions.type)::text) = ANY (ARRAY['DEPOSIT'::text, 'BANK_DEPOSIT'::text, 'REFUND'::text, 'CASH'::text, 'CREDIT'::text, 'BANK_TRANSFER_OR_MFS'::text, 'CHEQUE'::text])) THEN transactions.amount
                    ELSE (0)::double precision
                END) AS total_credit,
            sum(
                CASE
                    WHEN (upper((transactions.type)::text) = ANY (ARRAY['BOOKING_DEDUCTION'::text, 'DEDUCTION'::text, 'WITHDRAWAL'::text, 'ADMIN_CHARGE'::text, 'PURCHASE'::text])) THEN transactions.amount
                    ELSE (0)::double precision
                END) AS total_debit
           FROM public.transactions
          GROUP BY transactions.user_id) t ON ((u.id = t.user_id)));


