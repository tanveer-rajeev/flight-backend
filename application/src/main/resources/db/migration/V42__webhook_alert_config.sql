-- Configurable outbound webhook alerts (Discord, Slack, Teams, generic JSON)

CREATE TABLE IF NOT EXISTS public.webhook_alert_configs (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name character varying(150) NOT NULL,
    alert_type character varying(100) NOT NULL,
    channel character varying(50) NOT NULL,
    webhook_url text NOT NULL,
    description text,
    is_active boolean NOT NULL DEFAULT true,
    created_by bigint,
    updated_by bigint,
    created_at timestamp without time zone NOT NULL DEFAULT now(),
    updated_at timestamp without time zone
);

CREATE INDEX IF NOT EXISTS idx_webhook_alert_configs_alert_type
    ON public.webhook_alert_configs (alert_type);

CREATE INDEX IF NOT EXISTS idx_webhook_alert_configs_active
    ON public.webhook_alert_configs (is_active);

