package com.aerionsoft.application.enums.breakingnews;

public enum BreakingNewsTarget {
    /**
     * Visible to everyone (users + agencies)
     */
    ALL,
    /**
     * Visible only to individual users (non-agency)
     */
    USER,
    /**
     * Visible only to agency accounts
     */
    AGENCY,
    /**
     * Promotional offer visible to all audiences (users + agencies)
     */
    OFFER,
    /**
     * Promotion visible to all audiences (users + agencies)
     */
    PROMOTION,
}

