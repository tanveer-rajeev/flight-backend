-- Sales-person role, permissions, and business many-to-many relation

CREATE TABLE IF NOT EXISTS public.business_sales_persons (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    business_id bigint NOT NULL REFERENCES public.businesses(id),
    admin_user_id bigint NOT NULL REFERENCES public.admin_users(id),
    created_at timestamp(6) without time zone DEFAULT now(),
    CONSTRAINT uk_business_sales_person UNIQUE (business_id, admin_user_id)
);

CREATE INDEX IF NOT EXISTS idx_business_sales_persons_business_id
    ON public.business_sales_persons (business_id);

CREATE INDEX IF NOT EXISTS idx_business_sales_persons_admin_user_id
    ON public.business_sales_persons (admin_user_id);

-- Global sales-person role
INSERT INTO public.roles (is_active, name, slug, agency_id)
SELECT true, 'Sales Person', 'sales-person', NULL
WHERE NOT EXISTS (
    SELECT 1 FROM public.roles WHERE slug = 'sales-person' AND agency_id IS NULL
);

