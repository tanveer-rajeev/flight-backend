-- Branches lookup table, supplier assignment, and permissions

CREATE TABLE IF NOT EXISTS public.branches (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    agency_id bigint,
    name character varying(100) NOT NULL,
    description text,
    address character varying(255),
    phone_number character varying(15),
    is_active boolean NOT NULL DEFAULT true,
    is_deleted boolean NOT NULL DEFAULT false,
    created_by bigint NOT NULL,
    updated_by bigint,
    create_at timestamp without time zone NOT NULL DEFAULT now(),
    update_at timestamp without time zone,
    created_time_offset character varying(32),
    updated_time_offset character varying(32)
);

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_constraint WHERE conname = 'fk_branches_agency_id'
    ) THEN
        ALTER TABLE public.branches
            ADD CONSTRAINT fk_branches_agency_id
                FOREIGN KEY (agency_id) REFERENCES public.users (id);
    END IF;
END $$;

CREATE INDEX IF NOT EXISTS idx_branches_agency_id ON public.branches (agency_id);
CREATE INDEX IF NOT EXISTS idx_branches_is_deleted ON public.branches (is_deleted);

ALTER TABLE public.suppliers
    ADD COLUMN IF NOT EXISTS branch_id bigint;

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_constraint WHERE conname = 'fk_suppliers_branch_id'
    ) THEN
        ALTER TABLE public.suppliers
            ADD CONSTRAINT fk_suppliers_branch_id
                FOREIGN KEY (branch_id) REFERENCES public.branches (id);
    END IF;
END $$;

CREATE INDEX IF NOT EXISTS idx_suppliers_branch_id ON public.suppliers (branch_id);

